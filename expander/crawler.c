#include "../minishell.h"

int	stop_crawling(int c)
{
	if (ft_isspace(c) == 1 || (c >= 32 && c <= 47) || (c >= 58 && c <= 64) \
		|| (c > 90 && c < 97) || (c > 122 && c < 127) || c == '\0')
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
